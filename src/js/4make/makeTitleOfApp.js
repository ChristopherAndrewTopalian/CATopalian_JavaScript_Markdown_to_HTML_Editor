function makeTitleOfApp()
{
    let titleOfApp = ce("a");
    titleOfApp.style.position = 'absolute';
    titleOfApp.style.right = '7px';
    titleOfApp.style.top = '2px';
    titleOfApp.style.fontFamily = 'Arial';
    titleOfApp.style.fontSize = '15px';
    titleOfApp.style.color = 'rgb(130, 130, 130)';
    titleOfApp.style.fontWeight = 'bold';
    titleOfApp.style.textDecoration = 'none';
    titleOfApp.href = 'https://github.com/ChristopherAndrewTopalian/CATopalian_JavaScript_Markdown_to_HTML_Editor'
    titleOfApp.target = '_blank';
    titleOfApp.textContent = ' CATopalian JavaScript Markdown to HTML Editor';
    ba(titleOfApp);
}

